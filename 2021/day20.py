def min_max_tuple_list(tuple_list):
    min_x=min_y=math.inf
    max_x=max_y=-math.inf
    for e in tuple_list:
        min_x=min(e[0], min_x)
        max_x=max(max_x, e[0])
        min_y=min(e[1], min_y)
        max_y=max(max_y, e[1])

    return min_x, max_x, min_y, max_y

