def adjacency(coordinate, bounds=None, adjacency_type="compass", bounds_type="range"):
    for delta in itertools.product((-1,0,1), repeat=len(coordinate)):
        if adjacency_type=="compass":
            if sum(map(abs, delta)) > 1:
                # do not process if there are more dimensions
                continue
        
        if bounds is not None:
            in_bounds=True
            d_b=zip(delta, bounds)
            for i, (d,(low,high)) in enumerate(d_b):
                if bounds_type=="range":
                    in_bounds &= low <= coordinate[i] + d < high
                if bounds_type=="inclusive":
                    in_bounds &= low <= coordinate[i] + d <= high
                if bounds_type=="exclusive":
                    in_bounds &= low < coordinate[i] + d < high
                
                if not in_bounds:
                    continue

        yield tuple(c+d for c, d in zip(coordinate, delta))

def find_lit(input_image, enhancement_algo, generations):
    min_x, max_x, min_y, max_y=min_max_tuple_list(input_image)

    for g in range(generations):
        image=set()
        for r in range(min_x-1, max_x+2):
            for c in range(min_y-1, max_y+2):
                algo=[]
                for adj_x,adj_y in adjacency((r,c), adjacency_type="all"):
                    if adj_x < min_x or adj_x > max_x:
                        algo.append(g % 2 == 1)
                    elif adj_y < min_y or adj_y > max_y:
                        algo.append(g % 2 == 1)
                    else:
                        algo.append((adj_x, adj_y) in input_image)
                
                # get decimal
                v=bool_list_to_int(algo)
                # if algo says to light the pixel, then light it
                if enhancement_algo[v]:
                    image.add((r, c))
        
        input_image=image # for next gen
        min_x-=1
        min_y -= 1
        max_x += 1
        max_y += 1
    print('a', len(input_image))

