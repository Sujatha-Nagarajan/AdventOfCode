def find_reflections_b(lines, diff):
    for i in range(1, len(lines)):        
        a=lines[:i][::-1] # reverse
        b=lines[i:]
        
        # find min
        l=min(len(a), len(b))
        a=a[:l]
        b=b[:l]
        
        # find differences between corresponding lines in the arrays
        differences_list=[]
        for l,r in zip(a,b):
            differences_list.append(sum([ l[i] != r[i] for i in range(len(l)) ]))
        
        if sum(differences_list) == diff:
            return i
    return 0
        
def day13_b(diff):
    total=0
    patterns=input.split('\n\n')
    for pattern in patterns:
        lines=pattern.split('\n')
        r=find_reflections_b(lines, diff)
        v=find_reflections_b([*zip(*lines)], diff) # transpose of the matrix
        print(r,v)
        total+=v+(100*r)
    print(total)    
        

